public class Ex05_3 {
	public static void main(String[] args) {
		System.out.println(10 > 3);
		System.out.println(10 < 3);
		System.out.println(10 >= 3);
		System.out.println(10 <= 3);
		
		
		// (주의)
		System.out.println(10 == 3);
		System.out.println(10 != 3);
	}
}
